@dataclass
class RiskAssessment:
    """Risk assessment from Risk Manager"""
    approved: bool = False
    risk_score: float = 0.0  # 0-1
    max_position: float = 0.0
    reasons: List[str] = field(default_factory=list)
    warnings: List[str] = field(default_factory=list)


class Agent(ABC):
    """Base class for all Symbiote agents"""
    
    def __init__(self, agent_type: AgentType, name: str):
        self.agent_type = agent_type
        self.name = name
        self.status = AgentStatus.IDLE
        self.reputation = 1000
        self.total_trades = 0
        self.successful_trades = 0
        self.logger = logging.getLogger(f"symbiote.{agent_type.value}")
        
    @abstractmethod
    async def initialize(self) -> bool:
        """Initialize agent resources"""
        pass
    
    @abstractmethod
    async def run(self) -> Any:
        """Main agent loop"""
        pass
    
    @abstractmethod
    async def shutdown(self):
        """Cleanup resources"""
        pass
    
    def update_stats(self, success: bool):
        """Update agent statistics"""
        self.total_trades += 1
        if success:
            self.successful_trades += 1
            self.reputation = min(10000, self.reputation + 10)
        else:
            self.reputation = max(0, self.reputation - 10)
    
    @property
    def success_rate(self) -> float:
        if self.total_trades == 0:
            return 0.0
        return self.successful_trades / self.total_trades
    
    def to_dict(self) -> Dict:
        return {
            "type": self.agent_type.value,
            "name": self.name,
            "status": self.status.value,
            "reputation": self.reputation,
            "total_trades": self.total_trades,
            "successful_trades": self.successful_trades,
            "success_rate": self.success_rate
        }


class AgentMessage:
    """Message between agents"""
    
    def __init__(self, 
                 from_agent: str,
                 to_agent: str,
                 message_type: str,
                 payload: Dict):
        self.id = str(uuid.uuid4())
        self.timestamp = datetime.now()
        self.from_agent = from_agent
        self.to_agent = to_agent
        self.message_type = message_type
        self.payload = payload
    
    def to_dict(self) -> Dict:
        return {
            "id": self.id,
            "timestamp": self.timestamp.isoformat(),
            "from": self.from_agent,
            "to": self.to_agent,
            "type": self.message_type,
            "payload": self.payload
        }


class MessageBus:
    """Simple in-memory message bus for agent communication"""
    
    def __init__(self):
        self.subscribers: Dict[str, List[Agent]] = {}
        self.message_queue: asyncio.Queue = asyncio.Queue()
        
    def subscribe(self, agent_type: AgentType, agent: Agent):
        """Subscribe agent to message type"""
        if agent_type.value not in self.subscribers:
            self.subscribers[agent_type.value] = []
        self.subscribers[agent_type.value].append(agent)
        logger.info(f"{agent.name} subscribed to {agent_type.value}")
    
    async def publish(self, message: AgentMessage):
        """Publish message to subscribers"""
        await self.message_queue.put(message)
        logger.info(f"Message published: {message.from_agent} -> {message.to_agent}")
        
        # Deliver to subscribers
        if message.to_agent in self.subscribers:
            for agent in self.subscribers[message.to_agent]:
                await self.deliver(message, agent)
    
    async def deliver(self, message: AgentMessage, agent: Agent):
        """Deliver message to agent"""
        logger.debug(f"Delivering to {agent.name}: {message.message_type}")
        await agent.handle_message(message)


class SymbioteNetwork:
    """Main network coordinating all agents"""
    
    def __init__(self):
        self.agents: Dict[str, Agent] = {}
        self.message_bus = MessageBus()
        self.trades: List[Trade] = []
        self.signals: List[TradingSignal] = []
        self.logger = logging.getLogger("symbiote.network")
        
    def register_agent(self, agent: Agent):
        """Register agent in network"""
        self.agents[agent.name] = agent
        self.logger.info(f"Registered agent: {agent.name}")
        
    async def start_agent(self, agent_name: str):
        """Start an agent"""
        if agent_name in self.agents:
            agent = self.agents[agent_name]
            await agent.initialize()
            self.logger.info(f"Started agent: {agent_name}")
            
    async def stop_agent(self, agent_name: str):
        """Stop an agent"""
        if agent_name in self.agents:
            agent = self.agents[agent_name]
            await agent.shutdown()
            self.logger.info(f"Stopped agent: {agent_name}")
            
    async def start_all(self):
        """Start all registered agents"""
        for agent in self.agents.values():
            await agent.initialize()
            self.logger.info(f"Started: {agent.name}")
            
    async def stop_all(self):
        """Stop all agents"""
        for agent in self.agents.values():
            await agent.shutdown()
            self.logger.info(f"Stopped: {agent.name}")
            
    def get_status(self) -> Dict:
        """Get network status"""
        return {
            "agents": {name: agent.to_dict() for name, agent in self.agents.items()},
            "total_trades": len(self.trades),
            "total_signals": len(self.signals)
        }


